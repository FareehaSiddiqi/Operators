var score = 80;
switch (score) {
    case 100:
        console.log("Perfect Score");
        break;
    case 90:
        console.log("Excellent Score");
        break;
    case 80:
        console.log("Good Score");
        break;
    case 70:
        console.log("Passing Marks");
        break;
    default:
        console.log("Falling Score");
        break;
}
